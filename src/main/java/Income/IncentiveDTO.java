package Income;

public class IncentiveDTO {
	private String empNum;
	private int pay;
	private int addtional_pay;

	public String getEmpNum() {
		return empNum;
	}

	public void setEmpNum(String empNum) {
		this.empNum = empNum;
	}

	public int getPay() {
		return pay;
	}

	public void setPay(int pay) {
		this.pay = pay;
	}

	public int getAddtional_pay() {
		return addtional_pay;
	}

	public void setAddtional_pay(int addtional_pay) {
		this.addtional_pay = addtional_pay;
	}
}
