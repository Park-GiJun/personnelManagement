package HolidayApproval;

public class HolidayApprovalDTO {
	private String name;
	private String start_vacation;
	private String end_vacation;
	private String team;
	private String Approval;
	private String emp_num;
	private String emp_grade;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getStart_vacation() {
		return start_vacation;
	}
	public void setStart_vacation(String start_vacation) {
		this.start_vacation = start_vacation;
	}
	public String getEnd_vacation() {
		return end_vacation;
	}
	public void setEnd_vacation(String end_vacation) {
		this.end_vacation = end_vacation;
	}
	public String getTeam() {
		return team;
	}
	public void setTeam(String team) {
		this.team = team;
	}
	public String getApproval() {
		return Approval;
	}
	public void setApproval(String approval) {
		Approval = approval;
	}
	public String getEmp_num() {
		return emp_num;
	}
	public void setEmp_num(String emp_num) {
		this.emp_num = emp_num;
	}
	public String getEmp_grade() {
		return emp_grade;
	}
	public void setEmp_grade(String emp_grade) {
		this.emp_grade = emp_grade;
	}
	
	
}
