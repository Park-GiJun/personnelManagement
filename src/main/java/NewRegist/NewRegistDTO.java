package NewRegist;

public class NewRegistDTO {
	private String Name;
	private String Emp_num;
	private String Emp_grade;
	private String Team;
	private String Email;
	private String Pass;
	private String Phone;
	private String Work_start_day;
	private String Team_num;
	
	public String getTeam_num() {
		return Team_num;
	}
	public void setTeam_num(String team_num) {
		Team_num = team_num;
	}
	public String getEmail() {
		return Email;
	}
	public void setEmail(String email) {
		Email = email;
	}
	public String getPass() {
		return Pass;
	}
	public void setPass(String pass) {
		Pass = pass;
	}
	public String getPhone() {
		return Phone;
	}
	public void setPhone(String phone) {
		Phone = phone;
	}
	public String getWork_start_day() {
		return Work_start_day;
	}
	public void setWork_start_day(String work_start_day) {
		Work_start_day = work_start_day;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getEmp_num() {
		return Emp_num;
	}
	public void setEmp_num(String emp_num) {
		Emp_num = emp_num;
	}
	public String getEmp_grade() {
		return Emp_grade;
	}
	public void setEmp_grade(String emp_grade) {
		Emp_grade = emp_grade;
	}
	public String getTeam() {
		return Team;
	}
	public void setTeam(String team) {
		Team = team;
	}
}
