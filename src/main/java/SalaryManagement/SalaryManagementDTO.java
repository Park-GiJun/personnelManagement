package SalaryManagement;

public class SalaryManagementDTO {
	
	private String team;
	private String name;
	private String emp_num;
	private String emp_grade;
	private int pay;
	private int total_pay;
	private int incentive;
	private int holiday_pay;
	private int extra_work_pay;
	private int grade;
	private String phone;
	private String email;
	
	
	public String getTeam() {
		return team;
	}
	public void setTeam(String team) {
		this.team = team;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getEmp_num() {
		return emp_num;
	}
	public void setEmp_num(String emp_num) {
		this.emp_num = emp_num;
	}
	public String getEmp_grade() {
		return emp_grade;
	}
	public void setEmp_grade(String emp_grade) {
		this.emp_grade = emp_grade;
	}
	public int getPay() {
		return pay;
	}
	public void setPay(int pay) {
		this.pay = pay;
	}
	public int getTotal_pay() {
		return total_pay;
	}
	public void setTotal_pay(int total_pay) {
		this.total_pay = total_pay;
	}
	public int getIncentive() {
		return incentive;
	}
	public void setIncentive(int incentive) {
		this.incentive = incentive;
	}
	public int getHoliday_pay() {
		return holiday_pay;
	}
	public void setHoliday_pay(int holiday_pay) {
		this.holiday_pay = holiday_pay;
	}
	public int getExtra_work_pay() {
		return extra_work_pay;
	}
	public void setExtra_work_pay(int extra_work_pay) {
		this.extra_work_pay = extra_work_pay;
	}
	public int getGrade() {
		return grade;
	}
	public void setGrade(int grade) {
		this.grade = grade;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}

}
